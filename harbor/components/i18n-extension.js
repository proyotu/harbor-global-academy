export const i18nExtensionLabelsByCode = {
  de,
  en,
  ru,
  ro,
  cs,
  tr,
  el,
};

export function normalizeI18nCode(language) {
  const raw = String(language || '').trim().toLowerCase();

  if (!raw) {
    return 'de';
  }

  const match = LANGUAGE_CODE_BY_LABEL_PART.find(([, aliases]) => aliases.some((alias) => raw === alias || raw.includes(alias)));

  return match?.[0] || 'de';
}

export function getI18nExtensionLabels(language) {
  const code = normalizeI18nCode(language);

  if (code === 'de') {
    return { ...de };
  }

  return {
    ...de,
    ...en,
    ...(i18nExtensionLabelsByCode[code] || {}),
  };
}

export function createI18nTranslator(language, baseCopy = {}) {
  const labels = {
    ...getI18nExtensionLabels(language),
    ...baseCopy,
    ...getI18nExtensionLabels(language),
  };

  return (key, fallback = '') => {
    if (key && labels[key] !== undefined) {
      return labels[key];
    }

    if (fallback && labels[fallback] !== undefined) {
      return labels[fallback];
    }

    return fallback || key || '';
  };
}
